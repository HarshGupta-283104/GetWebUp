function scrollToCard(index) {
    const amount = index * getCardWidth();
    scrollContainer.scrollTo({ left: amount, behavior: 'smooth' });
}

function autoScroll() {
    const cardCount = getCardCount();
    const currentIndex = getCurrentCardIndex();
    const nextIndex = (currentIndex + 1) % cardCount;
    scrollToCard(nextIndex);
}

function scrollCards(direction = 'right') {
    const currentIndex = getCurrentCardIndex();
    const cardCount = getCardCount();
    
    let targetIndex;
    if (direction === 'right') {
        targetIndex = Math.min(currentIndex + 1, cardCount - 1);
    } else {
        targetIndex = Math.max(currentIndex - 1, 0);
    }
    
    scrollToCard(targetIndex);
}

function updateUI() {
    const scrollLeft = scrollContainer.scrollLeft;
    const maxScroll = scrollContainer.scrollWidth - scrollContainer.clientWidth;
    
    leftArrow.style.opacity = scrollLeft > 10 ? "1" : "0.3";
    rightArrow.style.opacity = scrollLeft < maxScroll - 10 ? "1" : "0.3";
    leftArrow.style.pointerEvents = scrollLeft > 10 ? "auto" : "none";
    rightArrow.style.pointerEvents = scrollLeft < maxScroll - 10 ? "auto" : "none";
    
    blurLeft.style.opacity = scrollLeft > 10 ? "1" : "0";
    blurRight.style.opacity = scrollLeft < maxScroll - 10 ? "1" : "0";
}

function startAutoScroll() {
    stopAutoScroll(); // Ensure no duplicate intervals
    autoScrollInterval = setInterval(autoScroll, 6000);
}

function stopAutoScroll() {
    if (autoScrollInterval) {
        clearInterval(autoScrollInterval);
        autoScrollInterval = null;
    }
}

// Debounced restart function to prevent multiple rapid restarts
let restartTimeout;
function restartAutoScrollAfterDelay(delay = 8000) {
    clearTimeout(restartTimeout);
    restartTimeout = setTimeout(startAutoScroll, delay);
}

leftArrow?.addEventListener("click", () => {
    stopAutoScroll();
    scrollCards('left');
    restartAutoScrollAfterDelay();
});

rightArrow?.addEventListener("click", () => {
    stopAutoScroll();
